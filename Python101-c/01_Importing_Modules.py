### Importing Modules
### In Python, a module is a file containing Python definitions and statements. The statement import is used to bring certain modules into your script, making the functions and classes they contain available to use.

### What is a module?
### A module can be thought of as a toolbox that contains tools (functions, classes, and variables) you can use in your script. For example, in your provided code, the random module is imported but not used. This module typically provides ways to generate random numbers, which isn't necessary for the grading function, so we can actually ignore it for now.

