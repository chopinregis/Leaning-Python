### Concept: Data Types
### Python has several built-in data types that commonly include integers, floats, strings, lists, tuples, sets, dictionaries, etc. Each type serves different purposes and behaves differently in various operations.

integer_type = 10  # Integer
float_type = 10.5  # Floating point number
string_type = "Hello"  # String
list_type = [1, 2, 3]  # List
tuple_type = (1, 2, 3)  # Tuple
set_type = {1, 2, 3}  # Set
dict_type = {'key': 'value'}  # Dictionary

