### Concept: Conditionals
### Conditionals in Python are used to execute different blocks of code based on certain conditions. The if statement is one of the most common conditional statements.

x = 5
if x > 0:
    print("x is positive")

### This will print "x is positive" because the condition x > 0 is true.

