### Concept: Printing
### The print() function in Python is used to output data to the standard output device (screen). You can also format the output using string literals or multiple arguments.

name = "Regis"
age = 30
print("Name:", name, "Age:", age)  # Output will be formatted with spaces by default
print(f"Name: {name}\nAge: {age}")

