### Concept: String Formatting with f-strings
### F-strings (formatted string literals) are a way to embed expressions inside string literals, using curly braces {}. This feature, available from Python 3.6+, makes it easy to format strings.

name = "Alice"
message = f"Hello, {name}!"
print(message)

