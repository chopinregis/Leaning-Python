### Concept: Function Definition
### In Python, functions are defined using the `def` keyword followed by the function name and parentheses that may include parameters. Functions allow you to encapsulate code for specific tasks that can be reused throughout your programs.

### Here's a basic example of a function:

def greet():
    print("Hello, world!")

greet()

### This function, named `greet`, prints "Hello, world!" when called.

