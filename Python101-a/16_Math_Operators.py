### Concept: Math Operators
### Python supports various math operators for performing arithmetic calculations like addition (+), subtraction (-), multiplication (*), division (/), modulo (%), exponentiation (**), and integer division (//).

a = 10
b = 3
print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # Multiplication
print(a / b)  # Division
print(a % b)  # Modulo
print(a ** b) # Exponentiation
print(a // b) # Integer division

### Exercise 11: Math Operators
### Use all the math operators listed to calculate with a = 9 and b = 4. Print each result with a description of what the calculation is doing (e.g., "9 plus 4 is 13").

a = 9
b = 4

print(a + b)  # 9 + 4 = 13
print(a - b)  # 9 - 4 = 5
print(a * b)  # 9 * 4 = 36
print(a / b)  # 9 / 4 = 2.25 (float division)
print(a % b)  # 9 % 4 = 1  (remainder)
print(a ** b) # 9 ** 4 = 6561  (9 raised to the power of 4)
print(a // b) # 9 // 4 = 2   (division without remainder)